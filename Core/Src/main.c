int scan_cvt=1;
void keyboard_send_ch(uint8_t ch,uint8_t  modi){
		int code;
    if( scan_cvt ){
        if( ch > 128 ){
            ch -=128;
        }
        code = _asciimap_eng[ch];
    }
    else
        code = ch;
	
	
		kbd_report.key1 = code&0x7F;
    kbd_report.modifier = modi;
		if ( code & 0x80) {                  // it's a capital letter or other character reached with shift
        kbd_report.modifier |= 0x02;    // the left shift modifier
    }
    
    USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t *)&kbd_report, sizeof(kbd_report));
    HAL_Delay(8);
    kbd_report.modifier = 0;
    kbd_report.key1 = 0;
    USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t *)&kbd_report, sizeof(kbd_report));
}


void keyboard_send_str(const char *str,const uint8_t modifier){
	int i;
	for(i = 0; i <= strlen(str) ; i++){
		keyboard_send_ch(str[i],modifier);
	  HAL_Delay(7);
	}
}

void openCommandPrompt(){
	keyboard_send_ch('r',8);
	HAL_Delay(300);
	keyboard_send_str("cmd",0);
	HAL_Delay(100);
	keyboard_send_ch(10,0);
}

void closeCommandPrompt(){
	keyboard_send_str("exit",0);
	HAL_Delay(100);
	keyboard_send_ch(10,0);
}


